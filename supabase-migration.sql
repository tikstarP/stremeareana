alter table public.chat_messages enable row level security;
alter table public.queue_entries enable row level security;
alter table public.game_scores enable row level security;
alter table public.leaderboard_entries enable row level security;
alter table public.art_submissions enable row level security;

-- Public read — anyone can view
create policy "Public profiles read" on public.profiles for select using (true);
create policy "Public rooms read" on public.rooms for select using (true);
create policy "Public chat read" on public.chat_messages for select using (true);
create policy "Public queue read" on public.queue_entries for select using (true);
create policy "Public scores read" on public.game_scores for select using (true);
create policy "Public leaderboard read" on public.leaderboard_entries for select using (true);
create policy "Public art read" on public.art_submissions for select using (true);

-- Authenticated insert/update/delete (fallback for direct client calls)
create policy "Users insert own profile" on public.profiles for insert with check (auth.uid() = id);
create policy "Users update own profile" on public.profiles for update using (auth.uid() = id);
create policy "Users insert chat" on public.chat_messages for insert with check (auth.uid() = user_id);
create policy "Users insert queue" on public.queue_entries for insert with check (auth.uid() = user_id);
create policy "Users delete own queue" on public.queue_entries for delete using (auth.uid() = user_id);
create policy "Users insert scores" on public.game_scores for insert with check (auth.uid() = user_id);
create policy "Users insert art" on public.art_submissions for insert with check (auth.uid() = user_id);

-- =============================================================
-- Storage bucket for art/fan-drop uploads
-- =============================================================

insert into storage.buckets (id, name, public)
values ('art-uploads', 'art-uploads', true)
on conflict (id) do nothing;

-- Allow public read
create policy "Public art files read"
on storage.objects for select using (bucket_id = 'art-uploads');

-- Allow authenticated uploads
create policy "Authenticated art files upload"
on storage.objects for insert with check (
  bucket_id = 'art-uploads'
  and auth.role() = 'authenticated'
);

-- =============================================================
-- Auto-create profile on user signup
-- =============================================================

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer set search_path = ''
as $$
begin
  insert into public.profiles (id, username, avatar_url, coins, points, streak)
  values (
    new.id,
    coalesce(split_part(new.email, '@', 1), 'User_' || substring(new.id::text, 1, 6)),
    'https://api.dicebear.com/7.x/avataaars/svg?seed=' || coalesce(split_part(new.email, '@', 1), new.id::text),
    200,
    0,
    0
  )
  on conflict (id) do nothing;
  return new;
end;
$$;

-- Trigger fires on every auth.users insert
drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_user();
