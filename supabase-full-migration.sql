-- =============================================================
-- ATOMIC LIKES FUNCTION (prevents race conditions)
-- =============================================================

create or replace function public.increment_room_likes(room_id bigint, user_id uuid)
returns integer
language plpgsql
security definer
as $$
begin
  insert into public.room_likes (room_id, user_id) values (room_id, user_id);
  update public.rooms set likes = likes + 1 where id = room_id;
  return (select likes from public.rooms where id = room_id);
exception
  when unique_violation then
    return (select likes from public.rooms where id = room_id);
end;
$$;

create or replace function public.decrement_room_likes(room_id bigint, user_id uuid)
returns integer
language plpgsql
security definer
as $$
begin
  delete from public.room_likes where room_id = increment_room_likes.room_id and user_id = increment_room_likes.user_id;
  update public.rooms set likes = greatest(0, likes - 1) where id = room_id;
  return (select likes from public.rooms where id = room_id);
end;
$$;

