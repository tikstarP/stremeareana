create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer set search_path = ''
as $$
begin
  insert into public.profiles (id, username, avatar_url, coins, points, streak)
  values (
    new.id,
    coalesce(split_part(new.email, '@', 1), 'User_' || substring(new.id::text, 1, 6)),
    'https://api.dicebear.com/7.x/avataaars/svg?seed=' || coalesce(split_part(new.email, '@', 1), new.id::text),
    200,
    0,
    0
  )
  on conflict (id) do nothing;
  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute function public.handle_new_user();

-- =============================================================
-- DEDUCT COINS (atomic, prevents overdraft)
-- =============================================================

create or replace function public.deduct_coins(user_id uuid, amount integer)
returns integer
language plpgsql
security definer
as $$
declare
  current_coins integer;
begin
  select coins into current_coins from public.profiles where id = user_id;
  if not found then
    raise exception 'Profile not found';
  end if;
  if current_coins < amount then
    raise exception 'Insufficient coins: have %, need %', current_coins, amount;
  end if;
  update public.profiles set coins = coins - amount where id = user_id;
  return current_coins - amount;
end;
$$;

-- =============================================================
-- SUBMIT SCORE (atomic, updates leaderboard + profile)
-- =============================================================

create or replace function public.submit_score(
  p_room_id bigint,
  p_user_id uuid,
  p_username text,
  p_game_type text,
  p_score integer
)
returns void
language plpgsql
security definer
as $$
declare
  won integer;
begin
  insert into public.game_scores (room_id, user_id, username, game_type, score)
  values (p_room_id, p_user_id, p_username, p_game_type, p_score);

  won := case when p_score >= 100 then 1 else 0 end;

  insert into public.leaderboard_entries (user_id, username, total_points, games_won, streak, period, avatar_url)
  values (
    p_user_id, p_username, p_score, won, won,
    'all',
    'https://api.dicebear.com/7.x/avataaars/svg?seed=' || coalesce(p_username, p_user_id::text)
  )
  on conflict (user_id, period) do update set
    total_points = leaderboard_entries.total_points + p_score,
    games_won = leaderboard_entries.games_won + won,
    streak = case when won = 1 then leaderboard_entries.streak + 1 else 0 end,
    username = p_username;

  update public.profiles set points = points + p_score where id = p_user_id;
end;
$$;

